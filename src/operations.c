#include "operations.h"

u32 fnv1a(u8* key, u32 bytes) {
	u32 hash = 0x811c9dc5;

	for (u32 i = 0; i < bytes; i++)
		hash = (hash ^ key[i]) * 0x01000193;

	return hash | (hash == 0);
}

